using System;
namespace Aritmético
{
	class OperatorsArithmetics
	{
		public static void Main(string[] args)
		{
			int soma1 = 10 + 20; //Retorna 30
			int soma2 = soma1 + 10; //Retorna 30 + 10 = 40
			int soma3 = soma2 + soma2; //Retorna 80

			Console.WriteLine(soma1);
			Console.WriteLine(soma2);
			Console.WriteLine(soma3);

			/*Operadores Aritméticos
			
			Adição + Soma dois valores
			
			Subtração - Subtraí um valor pelo outro
			
			Multiplicação * Multiplica dois valores
			
			Divisão / Divide um valor pelo outro
			
			Módulo % Retorna o resto da divisão
			
			Incremento ++ Aumenta o valor de uma variável em 1
			
			Decremento -- Reduz o valor de uma variável em 1
			
			*/
		}
	}
}